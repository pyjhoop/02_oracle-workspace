CREATE TABLE TB_USER(
    USER_NO NUMBER PRIMARY KEY,
    USER_NAME VARCHAR2(20) NOT NULL,
    USER_ID VARCHAR2(21) NOT NULL UNIQUE,
    USER_PWD VARCHAR2(22) NOT NULL
);

COMMENT ON COLUMN TB_USER.USER_NO IS '순번';
COMMENT ON COLUMN TB_USER.USER_NAME IS '이름';
COMMENT ON COLUMN TB_USER.USER_ID IS '아이디';
COMMENT ON COLUMN TB_USER.USER_PWD IS '비번';

CREATE TABLE TB_USER_INFO(
    USER_NO NUMBER PRIMARY KEY,
    USER_ADDRESS VARCHAR2(100),
    USER_PHONE VARCHAR2(13),
    USER_RSN VARCHAR2(20) NOT NULL UNIQUE,
    USER_GEN CHAR(3) CONSTRAINT GEN_CHECK CHECK(USER_GEN IN('남','여'))
);

